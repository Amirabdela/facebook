import 'package:firebase_core/firebase_core.dart';
import 'package:flutter/material.dart';
import 'fb_clone/login_page.dart' as fb_clone_login;
import 'fb_clone/feed_page.dart' as fb_clone_feed;
import 'fb_clone/profile_page.dart' as fb_clone_profile;
import 'firebase_options.dart'; // This file will be generated by flutterfire

void main() async {
  WidgetsFlutterBinding.ensureInitialized();
  // We try to initialize with DefaultFirebaseOptions if available, else just init.
  // Note: For Web/Windows, options are usually required.
  // The user will need to generate firebase_options.dart
  try {
     await Firebase.initializeApp(
       options: DefaultFirebaseOptions.currentPlatform,
     );
  } catch (e) {
      // If options file is missing, this might fail or we might try without options
      // But for this task avoiding a crash is better so we can show UI
      print("Firebase init failed (expected if config missing): $e");
      try {
        await Firebase.initializeApp(); 
      } catch (e2) {
         print("Firebase init without options failed: $e2");
      }
  }
  runApp(const MyApp());
}

class MyApp extends StatelessWidget {
  const MyApp({super.key});

  @override
  Widget build(BuildContext context) {
    return MaterialApp(
